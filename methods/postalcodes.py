def get_postal_code_regex(country_code):
    country_code = country_code.upper()
    return POSTAL_CODE_DATA.get(country_code, {}).get('regex')

def validate_postal_code(postal_code, country_code):
    regex = get_postal_code_regex(country_code)
    if regex is None or regex == '^$':
        return True
    return re.match(regex, postal_code.strip().split('.')[0]) is not None

